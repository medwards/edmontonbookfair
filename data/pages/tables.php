				<h3>call for vendors</h3>
				<p>Since 2002, the Edmonton Anarchist Bookfair has provided Anarchists, activists, and the simply curious the opportunity to learn, share ideas and connect with others. Vendors and speakers from across North America attend each year, with around a thousand attendees passing through during the event. Previous vendors have included AK Press and thoughtcrimeink. Previous speakers have included Ward Churchill and Allan Antliff.</p>
				<p>The 2008 Edmonton Anarchist Bookfair will again provide a forum to explore ideas and learn. It will be a festival of humanity and liberation, a warming beacon on the prairie. We are inviting vendors, alternative/independent publishers and social justice/radical groups to offer: </p>
