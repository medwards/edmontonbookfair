<h2>Radio as Activism – Becoming the Media</h2>
<p>Although getting in the corporate media is important to the progressive movement, utilizing underground and self-created media is important to spreading the message to underground movements in Edmonton and Canada.</p>
<p>CJSR offers training in recording your own events for broadcast over the CJSR airwaves, over the internet and across Canada.</p>
<p><b>Using the Media - "Why didn't they come out? I sent a press release."</b></p>
<p>This is a common phrase heard from advocacy groups. Learn how you can attract media to your event and develop media relations to ensure consistent coverage and create the message you're looking for.</p>
<p>This media workshop will work through the theory behind getting in the media and developing a message to work for your campaign all the way up to making the pitch call and writing the press release.</p>
<p>We run through a practice interview session and press scrum. Get to know the good, the bad and the ugly media.<br />
