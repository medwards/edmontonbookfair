<h2>When Workers Take Over</h2>
<p>This workshop will not be mere veneration of the struggle of Argentina’s recovered enterprises. Instead, the focus will be on extracting lessons from their experience and making concrete connections between the worker-controlled enterprises and our context here in Alberta. Rhiannon, utilizing some video and photos, will provide information about the context, history, and development of the worker run businesses and be available to answer questions. The objective of the workshop will be to have participants come away thinking about worker control not as a rusty discourse of the past, but rather as a vibrant and possible method of struggle for a better world.<br />
by <i>Rhiannon Edwards</i><br />
