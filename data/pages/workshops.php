<h2>Gender and Organizing</h2>
<p>An experiential workshop and discussion on gender construction, how we experience/express gender and how this affects organizing particularly in anti-authoritarian or non-hierarchical communities.</p>
<p>Over the past half year a number of groups have organically come together to talk about gender roles and sexism within activist communities and organizing in Edmonton. These discussions have occurred in groups of various sizes and in both formal and informal settings. Thus far there has not been an opportunity for open, public, inclusive and facilitated discussion and exploration of these issues.</p>
<p>Workshop goals</p>
<ul>
	<li>to provide a forum for discussion and exploration</li>
	<li>to pool our collective knowledge on how gender construction, expression and experience affects each of us</li>
	<li>to build a toolbox of language and different ways for expressing the diversity of gender expression and experience</li>
	<li>to think about ways to incorporate our knowledge into our everyday interactions</li>
	<li>to formulate ways to continue to this dialogue</li>
</ul>
<p>by <i>Nico (affiliated with Training for Change based out of Philadelphia) and Ariel B (local veteran of the Edmonton Community)</i><br />
