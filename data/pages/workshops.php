<h2>WITHER MARXISM? AN INTERACTIVE ROLE-PLAY</h2>
<p>The "leaders" of our society today (and many self-described "anarchists") would have you believe that Marxism has long been consigned to the dustbin of history, being sufficiently discredited with the collapse of the Soviet Empire in 1991. But are the works of Karl Marx really that irrelevant, or are there lessons we can still learn today?</p>
<p>This interactive workshop will briefly introduce participants to the basic tenets of Marx's Labour Theory of Value (LTV) and place them in a role-play situation simulating a typical work environment today. The direction of the workshop will depend almost entirely on those who attend, so if you want to see how 19th-century theory can still influence action today (and have some fun in the process), this workshop is for you!<br />
by <i>Alex C</i><br />
<b>5:45 - 7:00 pm<br />
Downstairs</b></p>
